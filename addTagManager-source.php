<?php

function google_tag_manager_hook($vars) { return <<<HTML

    <script>
        (function(w, d, s, l, i) {
            w[l] = w[l] || [];
            w[l].push({
                'gtm.start': new Date().getTime(),
                event: 'gtm.js'
            });
            var f = d.getElementsByTagName(s)[0],
                j = d.createElement(s),
                dl = l != 'dataLayer' ? '&l=' + l : '';
            j.async = true;
            j.src = 'https://www.googletagmanager.com/gtm.js?id=' + i + dl;
            f.parentNode.insertBefore(j, f);
        })(window, document, 'script', 'dataLayer', 'GOOGLE_CONTAINER_ID_GOES_HERE'); 
    </script>

HTML;
} add_hook("ClientAreaFooterOutput", 1, "google_tag_manager_hook");
